
a=25 # "=" is assignment operator to assign value to variable
a -=25 # "-=" is assignment operator to minus or subtract value from variable value which we give initally.
a +=25 # "-=" is assignment operator to plus or add value in variable value which we give initally.
a *=25 # "-=" is assignment operator to multiply value with variable value which we give initally.
a /=5 # "-=" is assignment operator to divide value with variable value which we give initally.

print(a)